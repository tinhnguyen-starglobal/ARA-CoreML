import UIKit

extension Constant {
    
    enum Color {
        
        enum Primary {
            static let blue = UIColor(hex: "#336CC8")
        }
        
        enum Secondary {
            static let gray = UIColor(hex: "#747480")
        }
        
        enum Neutral {
            static let fog = UIColor(hex: "#ECECEF")
            static let lightGray = UIColor(hex: "#8E8E93")
            static let clound = UIColor(hex: "#FAFAFC")
            static let white = UIColor(hex: "#FFFFFF")
            static let text = UIColor(hex: "#000000")
            static let link = UIColor(hex: "#53587A")
        }
        
        enum Background {
            static let background1 = UIColor(hex: "#FAFAF0")
            static let background2 = UIColor(hex: "#F5F4F8")
        }
    }
}

